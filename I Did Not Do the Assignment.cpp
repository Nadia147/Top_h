#include<bits/stdc++.h>
using namespace std;
bool f(int n){
    bool g;
    for(int i=2;i<=n/2;i++){
        if(n%i==0){
            g=true;
        }}
        return g;
    }
int main()
{
    int n;
    cin>>n;
    if(n==1){cout<<"I DID NOT DO THE ASSIGNMENT.\n";return 0;}


    if(f(n)==0){cout<<"NO PUNISHMENT\n";}
    else{
        for(int i=0;i<n;i++){cout<<"I DID NOT DO THE ASSIGNMENT.\n";}
    }
}
